CREATE DATABASE APIREST

USE APIREST;

Select * from TM_CATEGORIA

CREATE PROCEDURE SP_I_CATEGORIA_01
@CAT_ID INT,
@CAT_NAME NVARCHAR(100),
@CAT_DESC NVARCHAR(250)
AS 
BEGIN
	INSERT INTO TM_CATEGORIA
	(CAT_NAME,CAT_DESC)
	VALUES
	(@CAT_NAME, @CAT_DESC)

	SELECT * FROM TM_CATEGORIA
END

CREATE PROCEDURE SP_U_CATEGORIA_01
@CAT_ID INT,
@CAT_NAME NVARCHAR(100),
@CAT_DESC NVARCHAR(250)
AS 
BEGIN
	UPDATE TM_CATEGORIA
	SET 
		CAT_NAME=@CAT_NAME,
		CAT_DESC=@CAT_DESC

	WHERE CAT_ID = @CAT_ID

	SELECT * FROM TM_CATEGORIA
END

CREATE PROCEDURE SP_L_CATEGORIA_01
AS
BEGIN
SELECT * FROM TM_CATEGORIA
END

CREATE PROCEDURE SP_L_CAT_ID_CATEGORIA_01
 @CAT_ID INT
AS
BEGIN
SELECT * FROM TM_CATEGORIA
WHERE CAT_ID =  @CAT_ID
END
